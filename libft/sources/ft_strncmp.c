#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (!*s2 || !*s1)
		return (*s2 - *s1);
	while (n > 0)
	{
		if (*s1 != *s2 || !*s2 || !*s1)
			return (*s2 - *s1);
		n--;
		s1++;
		s2++;
	}
	return (0);
}

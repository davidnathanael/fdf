#include <string.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*s1;
	const char	*s2;

	s1 = (char *)dst;
	s2 = (const char *)src;
	while (n--)
		*s1++ = *s2++;
	return (dst);
}
